/** Plan the `mcp-auth` integration's files (config, the verifier seam, the battery wiring). */
function planMcpAuth(): GeneratedFile[] {
  return [
    { path: `${MCP_AUTH_DIR}/config.ts`, contents: configFile() },
    { path: `${MCP_AUTH_DIR}/verify.ts`, contents: verifyFile() },
    { path: `${MCP_AUTH_DIR}/governance.ts`, contents: governanceFile() },
  ];
}

/** The integrations `lesto add` implements today. */
const INTEGRATIONS = ["mcp-auth"] as const;

type Integration = (typeof INTEGRATIONS)[number];

/** The next-steps block each integration prints after its files are written. */
const NEXT_STEPS: Readonly<Record<Integration, readonly string[]>> = {
  "mcp-auth": MCP_AUTH_NEXT_STEPS,
};

/** True iff `value` names an integration this CLI implements today. */
function isIntegration(value: string | undefined): value is Integration {
  return value !== undefined && (INTEGRATIONS as readonly string[]).includes(value);
}

/**
 * Run a `lesto add <integration>` invocation.
 *
 * Validates the integration name up front — a missing or unknown name is refused by a
 * stable code before a single file is touched. Then, for each planned file, the SAME
 * idempotent write contract `g` keeps:
 *
 *   - `--dry-run`: print `would write <path>` (or `would skip <path>` for a path that
 *     already exists) and write nothing.
 *   - an existing file: print `exists <path> (unchanged)` if byte-identical, or
 *     `exists <path> (differs — left unchanged; edit or delete to regenerate)` if not —
 *     then SKIP it (never clobbered).
 *   - otherwise: write it and print `wrote <path>`.
 *
 * After a real run, the integration's next-steps are printed so the operator knows
 * exactly which TODOs to fill. Returns the process exit code (0 on a successful plan; a
 * refusal throws a coded {@link CliError} the caller turns into a non-zero exit).
 */
export async function runAdd(args: readonly string[], deps: AddDeps): Promise<number> {
  const [integration] = args;

  if (integration === undefined) {
    throw new CliError(
      "CLI_ADD_MISSING_INTEGRATION",
      `add needs an integration: lesto add <${INTEGRATIONS.join("|")}>`,
      {},
    );
  }

  if (!isIntegration(integration)) {
    throw new CliError(
      "CLI_ADD_UNKNOWN_INTEGRATION",
      `"${integration}" is not a known integration. Available: ${INTEGRATIONS.join(", ")}.`,
      { integration, known: [...INTEGRATIONS] },
    );
  }

  const dryRun = hasFlag(args, "dry-run");
  const files = planMcpAuth();

  for (const file of files) {
    if (dryRun) {
      // A dry run reports the REAL plan: an existing path would be skipped on a real run.
      deps.out(`would ${(await deps.exists(file.path)) ? "skip" : "write"} ${file.path}`);

      continue;
    }

    // Idempotency: an existing file is left exactly as it is — safe to re-run after the
    // author has edited what it first emitted. The message distinguishes a byte-identical
    // file (a true no-op) from one whose contents differ, so a re-run is never a silent
    // data-loss surprise.
    if (await deps.exists(file.path)) {
      const current = await deps.read(file.path);

      deps.out(
        current === file.contents
          ? `exists ${file.path} (unchanged)`
          : `exists ${file.path} (differs — left unchanged; edit or delete to regenerate)`,
      );

      continue;
    }

    await deps.write(file.path, file.contents);

    deps.out(`wrote ${file.path}`);
  }

  // The next steps are printed on a real run only — a dry run wrote nothing, so there is
  // nothing yet to configure or mount.
  if (!dryRun) for (const line of NEXT_STEPS[integration]) deps.out(line);

  return 0;
}
